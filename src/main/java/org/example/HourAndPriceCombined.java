package org.example;

public class HourAndPriceCombined {
    String hour;
    int price;


    public HourAndPriceCombined(String hour, int price) {
        this.hour = hour;
        this.price = price;
    }

    public String getHour() {
        return hour;
    }

    public int getPrice() {
        return price;
    }
}
